import Foundation

/**
 Font Family: Roboto
 - Roboto-Thin
 - Roboto-Bold
 - Roboto-Light
 - Roboto-Medium
 - Roboto-Regular
 */

enum Roboto: String, FontFamily {
    case thin    = "Roboto-Thin"
    case bold    = "Roboto-Bold"
    case light   = "Roboto-Light"
    case medium  = "Roboto-Medium"
    case regular = "Roboto-Regular"
}
